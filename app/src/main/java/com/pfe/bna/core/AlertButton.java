package com.pfe.bna.core;

/**
 * interface to hold the alert button identifiers
 */
public interface AlertButton {
    /**
     * integer to identify the alert ok button
     */
    int OK = 1;
    /**
     * integer to identify the alert yes button.
     */
    int YES = 2;
    /**
     * integer to identify the alert no button
     */
    int NO = 4;
    /**
     * integer to identify the alert cancel button.
     */
    int CANCEL = 8;
}
